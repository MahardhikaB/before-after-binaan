window.onclick = function(event) {
    if (event.target === modalProdukAdd) {
        modalProdukAdd.style.display = "none";
    }
    if (event.target === modalProdukEdit) {
        modalProdukEdit.style.display = "none";
    }
    if (event.target === modalProdukDelete) {
        modalProdukDelete.style.display = "none";
    }
    if (event.target === modalLegalitasAdd) {
        modalLegalitasAdd.style.display = "none";
    }
    if (event.target === modalLegalitasEdit) {
        modalLegalitasEdit.style.display = "none";
    }
    if (event.target === modalLegalitasDelete) {
        modalLegalitasDelete.style.display = "none";
    }
    if (event.target === modalMediaAdd) {
        modalMediaAdd.style.display = "none";
    }
    if (event.target === modalMediaEdit) {
        modalMediaEdit.style.display = "none";
    }
    if (event.target === modalMediaDelete) {
        modalMediaDelete.style.display = "none";
    }
    if (event.target === modalImage) {
        modalImage.style.display = "none";
    }
    if (event.target == modalEditPembinaan) {
        modalEditPembinaan.style.display = "none";
    }
    if (event.target == modalTambahPembinaan) {
        modalTambahPembinaan.style.display = "none";
    }
    if (event.target == modalDeletePembinaan) {
        modalDeletePembinaan.style.display = "none";
    }
};